function creatureSayCallback(cid, type, msg)
	if not npcHandler:isFocused(cid) then
		return false
	end
	
	if msgcontains(msg, "old backpack") or msgcontains(msg, "backpack") or msgcontains(msg, "sam backpack") then
		npcHandler:say("O que? Você está me dizendo que encontrou a mochila do meu velho aventureiro que perdi anos atrás?", cid)
		npcHandler.topic[cid] = 1
		
-----------------------------------------------------------------------------------

	elseif msgcontains(msg, "yes") then
		if npcHandler.topic[cid] == 1 then	
			if getPlayerItemCount(cid, 3960) >= 1 then -- Sam's Old Backpack Quest
				if doPlayerRemoveItem(cid, 3960, 1) then -- Sam's Old Backpack Quest
					if getPlayerStorageValue(cid, 45271) <= 0 then
						setPlayerStorageValue(cid, 45271, 1)
					end
					npcHandler:say({"Muito obrigado! Isso traz de volta boas lembranças antigas! Por favor, como recompensa, pegue esta armadura especial. ...",
									"Vou anotar seu nome imediatamente. Que os deuses sejam bons contigo!"}, cid, 4000)
					doPlayerAddItem(cid, 2503, 1)
					npcHandler.topic[cid] = 0
				else
					doPlayerSendTextMessage(cid,25,"ERROR! Please contact the administrator.")
					npcHandler.topic[cid] = 0
				end
			else
				npcHandler:say("You don't have it...", cid)
				npcHandler.topic[cid] = 0
			end	
		end
		
	elseif msgcontains(msg, "time") then
	   local time = getTibiaTime()
	   npcHandler:say("Claro! Agora são: ".. time.hours ..":".. time.minutes ..".", cid)
	   npcHandler.topic[cid] = 0
	end
	return true
end

